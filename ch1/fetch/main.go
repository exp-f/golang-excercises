// Fetch prints the content found at each specified URL.
// Упражнения 1.7 - 1.9
package main

import (
	"fmt"
	"io"
	"net/http"
	"os"
	"strings"
)

func main() {
	for _, url := range os.Args[1:] {
		// Они просили добавить только автоматическое добавление http://, но сейчас 2022 год, все уже перешли на https
		if !strings.HasPrefix(url, "https://") {
			url = "https://" + url
		}
		resp, err := http.Get(url)
		if err != nil {
			fmt.Fprintf(os.Stderr, "fetch: %v\n", err)
			os.Exit(1)
		}
		bytes_count, err := io.Copy(os.Stdout, resp.Body)

		resp.Body.Close()
		if err != nil {
			fmt.Fprintf(os.Stderr, "fetch: reading %s: %v\n", url, err)
			os.Exit(1)
		}
		fmt.Printf("%d bytes copied \n", bytes_count)
		fmt.Printf("Status code: %s", resp.Status)
	}
}
